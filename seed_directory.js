async function addSite(site) {
  const payload = {
    adminUsername: ADMIN_USER,
    adminPassword: ADMIN_PASS,
    ...site,
  };

  try {
    const res = await fetch(`${BASE_URL}/api/directory/add`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload),
    });
    const data = await res.json();
    if (data.success) {
      console.log(`✅ أضيف: ${site.name}`);
    } else {
      console.log(`⚠️  تخطي: ${site.name} — ${data.error}`);
    }
  } catch (e) {
    console.error(`❌ خطأ في إضافة ${site.name}:`, e.message);
  }
}

async function main() {
  console.log('🚀 بدء إضافة محتوى دليل الدردشات...\n');
  for (const site of sites) {
    await addSite(site);
    // تأخير صغير بين كل إضافة
    await new Promise(r => setTimeout(r, 150));
  }
  console.log(`\n🎉 تم! أضيف ${sites.length} موقع إلى الدليل`);
}

main();
